public class Calculadora {
   public Integer multiplicacao(int A, int B){
       return A * B;
   }
   
   public Integer soma(int A, int B){
       return A + B;
   }
   
   public Integer subtracao(int A, int B){
       return A - B;
   }
   
   public Integer divisao(int A, int B){
       return A / B;
   }
           
}
